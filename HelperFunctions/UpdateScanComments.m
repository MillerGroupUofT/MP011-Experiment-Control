function [] = UpdateScanComments()

    % Load handles from base workspace
    handles = evalin('base','handles');
    
    % Update scan comment
    handles.scanComments = '';
    if(get(handles.figGUI_panScan2_transl_check, 'Value') > 0)
        
        handles.scanComments = sprintf('%sSample Translation Horizontal Range (%sm): [%s:%s:%s]\n', ...
            handles.scanComments, ...
            char(181), ...
            handles.scanHoriMin,...
            handles.scanHoriStep,...
            handles.scanHoriMax...
            );
        handles.scanComments = sprintf('%sSample Translation Vertical Range (%sm): [%s:%s:%s]\n', ...
            handles.scanComments, ...
            char(181), ...
            handles.scanVertMin,...
            handles.scanVertStep,...
            handles.scanVertMax...
            );
        handles.scanComments = sprintf('%sSample Translation Z Range (%sm): [%s:%s:%s]\n', ...
            handles.scanComments, ...
            char(181), ...
            handles.scanZMin,...
            handles.scanZStep,...
            handles.scanZMax...
            );

    end

    if(get(handles.figGUI_panScan3_time_check, 'Value') > 0)

        handles.scanComments = sprintf('%sTime Delay Points (ps): %s\n', ...
            handles.scanComments, ...
            get(handles.figGUI_panScan3_Time_edit, 'String')...
            );

        handles.scanComments = sprintf('%sTime Delay Stage Positions (mm): %s\n', ...
            handles.scanComments, ...
            get(handles.figGUI_panScan3_TimePos_edit, 'String')...
            );

        handles.scanComments = sprintf('%sTime Delay Stage Zero Position (mm): %s\n', ...
            handles.scanComments, ...
            handles.scanTimeZeroPos...
            );

    end

    if(get(handles.figGUI_panScan4_rot_check, 'Value') > 0)

        handles.scanComments = sprintf('%sSample Rotation Points (deg): %s\n', ...
            handles.scanComments, ...
            get(handles.figGUI_panScan4_Rot_edit, 'String')...
            );

        handles.scanComments = sprintf('%sRotation Stage Zero Position (deg): %s\n', ...
            handles.scanComments, ...
            handles.scanRotZeroPos...
            );
    end
    set(handles.figGUI_panScan1_scanComment, 'String', handles.scanComments);
    
    % Save handles in base workspace
    assignin('base','handles',handles)

end
